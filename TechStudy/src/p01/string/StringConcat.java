package p01.string;

public class StringConcat {

	public static void main(String[] args) {
		String str1 = "안녕";
		String str2 = "하세요";
	
		String str3 = str1.concat(str2);
		
		System.out.println("str1 : "+str1);
		System.out.println("str2 : "+str2);
		System.out.println("srt1과 str2를 concat : "+str3);
		
		
	}

}
